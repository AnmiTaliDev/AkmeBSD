const FbInfo = struct {
    addr: [*]u32 = undefined,
    width: u64 = 0,
    height: u64 = 0,
    pitch_pixels: u64 = 0,
    pitch: u64 = 0,
    char_w: u32 = 8,
    char_h: u32 = 16,
    bg: u32 = 0x0A0A0A,
    fg: u32 = 0xE6E6E6,
    cx: u32 = 0,
    cy: u32 = 0,
    ready: bool = false,
};

var fb: FbInfo = .{};

pub fn init() void {
    if (fb.ready) return;
    const resp = fb_request.response orelse return;
    if (resp.framebuffer_count == 0) return;
    const f = resp.framebuffers.?[0] orelse return;
    fb.addr = @ptrCast(@alignCast(f.address orelse return));
    fb.width = f.width;
    fb.height = f.height;
    fb.pitch = f.pitch;
    fb.pitch_pixels = f.pitch / 4;
    fb.char_h = fb_mod.fontHeight();
    fb.ready = true;
    clear();
}

fn putPixel(x: u32, y: u32, color: u32) void {
    if (x >= fb.width or y >= fb.height) return;
    fb.addr[y * fb.pitch_pixels + x] = color;
}

fn drawChar(x: u32, y: u32, c: u8, color: u32) void {
    const glyph = fb_mod.glyphFor(c);
    const h = fb_mod.fontHeight();
    for (0..h) |row| {
        const bits = glyph[row];
        for (0..8) |col| {
            if (bits & (@as(u8, 0x80) >> @as(u3, @intCast(col))) != 0) {
                putPixel(x + @as(u32, @intCast(col)), y + @as(u32, @intCast(row)), color);
            }
        }
    }
}

pub fn clear() void {
    if (!fb.ready) return;
    for (0..fb.height) |y| {
        for (0..fb.width) |x| {
            putPixel(@intCast(x), @intCast(y), fb.bg);
        }
    }
    fb.cx = 0;
    fb.cy = 0;
}

fn newline() void {
    fb.cx = 0;
    fb.cy += fb.char_h;
    if (fb.cy + fb.char_h > fb.height) {
        const scroll_bytes = @as(usize, fb.char_h) * @as(usize, fb.pitch);
        const remaining_bytes = (@as(usize, fb.height) - fb.char_h) * @as(usize, fb.pitch);
        const src: [*]u8 = @ptrCast(fb.addr);
        @memmove(src[0..remaining_bytes], src[scroll_bytes .. scroll_bytes + remaining_bytes]);
        const clear_start: [*]u32 = @ptrFromInt(@intFromPtr(fb.addr) + remaining_bytes);
        const clear_count = scroll_bytes / 4;
        for (0..clear_count) |i| clear_start[i] = fb.bg;
        fb.cy = @intCast(fb.height - fb.char_h);
    }
}

pub fn putchar(c: u8, color: u32) void {
    if (!fb.ready) return;
    if (c == '\n') { newline(); return; }
    if (c == '\r') { fb.cx = 0; return; }
    if (c == '\t') { fb.cx += fb.char_w * 4; return; }
    if (c == '\x08') {
        backspace();
        return;
    }
    if (fb.cx + fb.char_w > fb.width) newline();
    drawChar(fb.cx, fb.cy, c, color);
    fb.cx += fb.char_w;
}

pub fn backspace() void {
    if (!fb.ready or fb.cx == 0) return;
    fb.cx -= fb.char_w;
    for (0..fb.char_h) |row| {
        for (0..fb.char_w) |col| {
            putPixel(fb.cx + @as(u32, @intCast(col)), fb.cy + @as(u32, @intCast(row)), fb.bg);
        }
    }
}

pub fn print(s: []const u8, vga_color: u8) void {
    init();
    const color = vga_colors[vga_color & 0xF];
    for (s) |c| putchar(c, color);
}

pub fn drawLogoRow(logo: []const u32, logo_w: u32, logo_h: u32) void {
    if (!fb.ready or logo.len == 0) return;
    const spacing = (fb.width - logo_w) / 2;
    for (0..logo_h) |row| {
        for (0..logo_w) |col| {
            const px = logo[row * logo_w + col];
            if (px == 0) continue;
            putPixel(@intCast(spacing + col), @intCast(row), 0xFF000000 | px);
        }
    }
}

pub fn getBuiltinFont() *const [128][8]u8 {
    return &builtin_font;
}
